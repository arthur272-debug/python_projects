# Operações  sobre conjuntos

conjunto1 = {1, 2, 3}
conjunto2 = {2, 3, 4}
print(conjunto1)
print(conjunto2)

conjunto3 = conjunto1 | conjunto2
print('1:', conjunto3)
conjunto3 = conjunto1 & conjunto2
print('2:', conjunto3)
conjunto3 = conjunto2 - conjunto1
print('3:', conjunto3)
conjunto3 = conjunto1 ^ conjunto2
print('4:', conjunto3)
